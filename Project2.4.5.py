travel_list = ['Paris', 'Rome', 'New York', 'Singapore', 'Iceland']
print(travel_list) # print original list
print(sorted(travel_list)) # print original ist in alphabetical order
print(travel_list)  # print original list
print("-------------------------------------------------------------")
sorted_travel_list = sorted(travel_list) # store sorted list in another variable
sorted_travel_list.reverse()
print(sorted_travel_list)
print(travel_list) # print original list
print("-------------------------------------------------------------")
travel_list.reverse() # reverse the order of the list
print(travel_list) # print travel_list
travel_list.reverse() # reverse the order of the list
print(travel_list)
print("-------------------------------------------------------------")
travel_list.sort()
print(travel_list)
travel_list.sort(reverse=True)
